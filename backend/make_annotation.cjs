const {
  Document, Packer, Paragraph, TextRun, AlignmentType, LineRuleType
} = require('docx');
const fs = require('fs');

const FONT = 'Times New Roman';
const SIZE = 28; // 14pt = 28 half-points

const spacing = {
  line: 360, // 1.5 интервал = 240 * 1.5 = 360
  lineRule: LineRuleType.AUTO,
  before: 0,
  after: 0,
};

function par(runs, align = AlignmentType.JUSTIFY) {
  return new Paragraph({
    alignment: align,
    spacing,
    children: runs,
  });
}

function run(text, bold = false) {
  return new TextRun({ text, bold, font: FONT, size: SIZE });
}

function empty() {
  return new Paragraph({ spacing: { before: 0, after: 0, line: 360, lineRule: LineRuleType.AUTO }, children: [new TextRun({ text: '', font: FONT, size: SIZE })] });
}

